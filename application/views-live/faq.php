<script type="text/javascript">
	$(function() {
		initFaqFunction();
	});

	function initFaqFunction() {
		$('.faq-category .category-list').click(function() {
			$('.faq-category .category-list').removeClass('active');
			$(this).addClass('active');

			const categoryIdx = $(this).attr('data-idx');

			$('.faq-list').removeClass('active');
			$('.faq-list-'+ categoryIdx).addClass('active');
		});

		$('.faq-panel').click(function() {
			if ($(this).hasClass('active')) {
				$(this).removeClass('active');
			}
			else {
				$('.faq-panel').removeClass('active');
				$(this).addClass('active');
			}
		});

		$('.track-order').click(function() {
			const orderNumber = $('input[name="order-number"]').val();

			if (orderNumber == '') {
				return;
			}

			$('.track-order').html('<i class="fa fa-circle-o-notch fa-spin fa-fw"></i> Loading...');

			openTracking(orderNumber);
		});
	}

	function openTracking(orderNumber) {
		$.ajax({
            data: {
            	order_number: orderNumber,
                "<?= $csrf['name'] ?>": "<?= $csrf['hash'] ?>"
            },
            dataType: 'JSON',
            error: function() {
            	alert('Server Error.');
            	$('.track-order').html('Go');
            },
            success: function(data) {
                if (data.status == 'success') {
                	$('.modal-order-track-container').empty().append(data.order_tracking);
                	$('#modal-order-track').modal('show');
                }
                else {
                    alert(data.message);
                }

                $('.track-order').html('Go');
            },
            type : 'POST',
            url : '<?= base_url(); ?>ajax/ajax_track_order/'
        });
	}
</script>
